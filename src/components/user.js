export class User {
  
  constructor(idUsuario, usuario, nombre, apellido, gerencia, estado, correo) {
    this.idUsuario = idUsuario; 
    this.usuario = usuario; 
    this.nombre = nombre;
    this.apellido = apellido; 
    this.gerencia = gerencia;
    this.estado = estado;
    this.correo = correo;
  }
}

